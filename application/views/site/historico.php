									<h2>Histórico</h2>
								</header>
								<img style="float: right; margin-left: 15px;" src="<?= base_url('assets/site/') ?>images/imagem1.jpg">
								<p>Destacamos que o Congresso “Pedagogia Histórico-Crítica: em defesa da Escola Pública e Democrática em tempos de Projetos de ‘Escolas sem Partidos’” objetiva promover a interlocução científica entre professores, pesquisadores e estudantes de diversos Estados brasileiros (São Paulo, Espírito Santo, Bahia, Paraná, Minas Gerais, Goiás, Mato Grosso, Pará, Pernambuco, etc.) vinculados ao campo teórico da Pedagogia Histórico-crítica e, principalmente, propiciar reflexões sobre o papel da Educação Escolar na formação e no desenvolvimento do ser humano. Podendo, assim, compartilhar e socializar os estudos, pesquisas e reflexões no campo da Educação Escolar com professores que atuam em redes públicas de ensino, com pesquisadores e professores que atuam no ensino superior, com estudantes de cursos de licenciaturas e estudantes de cursos de Pós-graduação em Educação, contribuindo para com o aprimoramento da prática pedagógica no âmbito da Educação Básica (Educação Infantil; Ensino Fundamental I e II), Ensino Médio, Ensino Superior e para com o desenvolvimento da pesquisa científica no Brasil.</p>
								<p>Esse congresso dará continuidade a três importantes eventos que abordaram a Pedagogia Histórico-Crítica como núcleo central dos encontros. O primeiro evento realizado foi o Seminário “Pedagogia Histórico-Crítica: 30 anos” em 2009 na UNESP de Araraquara/S.P. O segundo evento realizado foi o Congresso “Infância e Pedagogia Histórico-Crítica” no ano de 2012 na UFES/E.S. O terceiro evento a ser destacado foi o Congresso “Pedagogia Histórico-Crítica: educação e desenvolvimento humano” realizado em 2015 na UNESP de Bauru/S.P., sendo que nesse evento obtivemos um total de 892 inscritos com representação de 20 Estados do País, sendo que houve um expressivo número de professores da Educação Básica, estudantes de graduação e pós-graduação e docentes de diversas universidades que participaram do evento.<br>
								</p>
								<p>Destacamos, ainda, o evento ocorrido em 2016 na UFES (Vitória/E.S.), o qual não compõem a trajetória dos encontros da Pedagogia Histórico-crítica, mas que é 	significativo ao coletivo de autores e pesquisadores dessa teoria. O evento na UFES, em 2016, objetivou comemorar os 50 anos de carreira docente e atuação profissional do profº Dermeval Saviani em formato de Seminário: “Dermeval Saviani e a Educação Brasileira: Construção Coletiva da Pedagogia Histórico-Crítica”. Foi durante a realização desse evento que ocorreu a indicação do local do próximo Congresso Nacional da Pedagogia Histórico-crítica para ser realizado no ano de 2018 na FCT/UNESP (Pres. Prudente/S.P.).
								</p>
								<p>ssim, ao longo desses anos (2009 a 2016) os eventos voltados às reflexões propiciadas a partir dos aportes da Pedagogia Histórico-crítica deram continuidade à proposta de elaboração coletiva dos pressupostos teórico-metodológicos dessa teoria, haja vista a efetiva e expressiva participação de professores de sistemas públicos, estudantes de cursos de licenciaturas e pós-graduação, professores/pesquisadores de várias áreas que atuam no ensino público superior ao longo dos três congressos realizados (2009; 2012; 2015) e do evento comemorativo (2016). Nessa trajetória coletiva, o Congresso <strong>“Pedagogia Histórico-Crítica: em defesa da Escola Pública e Democrática em tempos de Projetos de ‘Escolas sem Partidos’”</strong> ocorrerá em momento econômico-político histórico em nosso país, principalmente por vivenciarmos uma total desvalorização da escola pública e da profissão docente em tempos de propagação de projetos que defendem o fim da essência da educação escolar – sendo que a educação escolar visa desenvolver o ser humano a partir da apropriação dos conhecimentos científicos presentes nos currículos escolares e ensinados pelos professores.</p>
