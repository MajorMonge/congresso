							<h3 style="font-size: 16pt; color:black; font-weight: bold;">1 - Quem pode enviar o trabalho?</h3><br>
							<p>Graduandos, pós-graduandos, professores de todos os níveis de ensinos e profissionais de outras áreas que tenham relação com a temática do evento.</p>
							<p>A submissão do trabalho deve ser feita respeitando as <strong>normas de submissão e prazos estabelecidos.</strong></p>
							<h3 style="font-size: 16pt; color:black; font-weight: bold;">2 - Quais são as regras para submissão do trabalho?</h3><br>
							<p>Serão aceitos os trabalhos de cunho acadêmico conforme os Eixos Temáticos do Congresso, sob a forma de duas modalidades: <strong>Resumo Expandido e Trabalho Completo.</strong></p>
<p>
A apresentação dos trabalhos aprovados (<strong>Resumo Expandido e/ou Trabalho Completo</strong>) será no formato de comunicação oral (*não haverá modalidade de pôster).</p>
<p>
Em qualquer uma das duas modalidades, os trabalhos inscritos deverão atender aos seguintes objetivos:</p>
<p>
<strong>a)</strong> ter a Pedagogia Histórico-Crítica como objeto de pesquisa e/ou fundamento teórico de investigações e de experiências que visem contribuições propositivas a ela; ou que a analisem criticamente à vista de seu aprimoramento;</p>
<p>
<strong>b)</strong> adotar os fundamentos epistemológicos, filosóficos e ontológicos que fundamentam a pedagogia histórico-crítica na análise da relação entre educação e desenvolvimento humano.</p>
							
