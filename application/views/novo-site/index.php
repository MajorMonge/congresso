<p class="apresentacao">
	Destacamos que o Congresso “Pedagogia Histórico-Crítica: em defesa da
	Escola Pública e Democrática em tempos de Projetos de ‘Escolas sem
	Partidos’” objetiva promover a interlocução científica entre professores,
	pesquisadores e estudantes de diversos Estados brasileiros vinculados ao
	campo teórico da Pedagogia Histórico-crítica e, principalmente, propiciar
	reflexões sobre o papel da Educação Escolar na formação e no
	desenvolvimento do ser humano. Podendo, assim, compartilhar e socializar
	os estudos, pesquisas e reflexões no campo da Educação Escolar com
	professores que atuam em redes públicas de ensino, com pesquisadores e
	professores que atuam no ensino superior, com estudantes de cursos de
	licenciaturas e estudantes de cursos de Pós-graduação em Educação,
	contribuindo para com o aprimoramento da prática pedagógica no âmbito da
	Educação Básica (Educação Infantil; Ensino Fundamental I e II), Ensino
	Médio, Ensino Superior e para com o desenvolvimento da pesquisa
	científica no Brasil.
	<br>
	<br>
	Esse congresso dará continuidade a três importantes eventos que abordaram
	a Pedagogia Histórico-Crítica como núcleo central dos encontros. O
	primeiro evento realizado foi o Seminário “Pedagogia Histórico-Crítica:
	30 anos” em 2009 na UNESP de Araraquara/S.P. O segundo evento realizado
	foi o Congresso “Infância e Pedagogia Histórico-Crítica” no ano de 2012
	na UFES/E.S. O terceiro evento a ser destacado foi o Congresso “Pedagogia
	Histórico-Crítica: educação e desenvolvimento humano” realizado em 2015
	na UNESP de Bauru/S.P., sendo que nesse evento obtivemos um total de 892
	inscritos com representação de 20 Estados do País, sendo que houve um
	expressivo número de professores da Educação Básica, estudantes de
	graduação e pós-graduação e docentes de diversas universidades que
	participaram do evento.
</p>

