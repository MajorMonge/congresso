<h1 class="brandon_n animate fadeInLeft">PEDAGOGIA HISTÓRICO-CRÍTICA</h1>
<div class="row animate fadeIn" id="ghistorico">
	<div class="col m5 s12 orn" style="padding-bottom: -50%;">
		<p class="intro brandon_r">HISTÓRICO DO EVENTO</p>
	</div>
</div>

<p class="apresentacao">
	Considerando o processo de elaboração coletiva da Pedagogia Histórico-crítica,
	convidamos os professores que atuam nos vários níveis de ensino (Educação Infantil, Ensino
	Fundamental, Ensino Médio e Ensino Superior), os graduandos de cursos de Pedagogia e cursos
	de licenciaturas, os mestrandos e doutorandos de Programas de Pós-graduação e demais
	profissionais interessados em refletir sobre a importância da Educação Escolar na promoção do
	desenvolvimento humano para participarem do Congresso <b>“Pedagogia Histórico-Crítica: em
	defesa da Escola Pública e Democrática em tempos de Projetos de ‘Escolas sem Partidos”</b>,
	a ser realizado de <b>11 a 13.07.2018</b>, na F.C.T./UNESP de Presidente Prudente-S.P.
</p>
<p class="apresentacao">
	Destacamos que o primeiro evento dessa teoria pedagógica ocorreu em 2019 na
	FCL/UNESP de Araraquara, no formato de Seminário com o tema <b>“Pedagogia Histórico-Crítica:
	30 anos”</b>. Um dos objetivos do Seminário foi comemorar e compartilhar os trinta anos de estudos
	e produções coletivas da Pedagogia Histórico-crítica, uma teoria pedagógica contra hegemônica e
	revolucionária. Marsiglia (2011, p. 27-28)1 destaca que no Seminário: “[...] se reuniram
	professores e alunos de graduação e pós-graduação de 69 instituições, 37 cidades, 11 Estados
	brasileiros. Isso indica que os educadores continuam discutindo sobre alternativas pedagógicas
	que respondam a uma educação crítica na formação dos indivíduos”.
</p>
<p class="apresentacao">
	Após o evento em 2009, a construção coletiva da pedagogia Histórico-crítica
	prosseguiu fortemente, sendo que as reflexões propiciadas no Seminário foram materializadas no
	livro Pedagogia histórico-crítica: 30 anos, uma publicação de 2011.
</p>
<p class="apresentacao">
	O segundo evento acadêmico a ser destacado ocorreu no formato de Congresso, em
	2012, na Universidade Federal do Espírito Santo (UFES/ES) cujo tema foi <b>“Infância e Pedagogia
	Histórico-Crítica”</b>, objetivando abordar a temática da infância à luz das elaborações teóricas que
	fundamentam a Pedagogia Histórico-Crítica.
</p>
<p class="apresentacao">
	No processo de continuidade dos eventos anteriores (2009; 2012), no ano de 2015
	ocorreu o Congresso <b>“Pedagogia histórico-crítica: educação e desenvolvimento humano”</b>,
	realizado na FC/UNESP de Bauru/S.P., que objetivou apresentar as contribuições da Pedagogia
	Histórico-Crítica para a promoção do desenvolvimento humano por meio da educação escolar.
	Nesse evento houve a participação de 892 pessoas com a representação de vinte (20) Estados do
	País e com a expressiva participação de professores da Educação Básica, estudantes de
	graduação e pós-graduação, além de professores universitários.
</p>
<p class="apresentacao">
	Outro evento de destaque ocorreu na UFES/E.S em 2016. O objetivo do Seminário
	foi o de comemorar os cinquenta (50) anos de carreira do professor Dermeval Saviani. O tema do
	Seminário foi <b>“Dermeval Saviani e a Educação Brasileira: Construção Coletiva da Pedagogia
	Histórico-Crítica”</b>. O evento reuniu 350 pessoas com inscrições pagas e 150 pessoas com
	inscrições gratuitas (estudantes e professores da rede de educação básica). Ao longo de 2009 a
	2016 os eventos voltados ao debate da Pedagogia Histórico-crítica efetivaram um de seus
	propósitos que é a elaboração coletiva de seus princípios, haja vista a participação expressiva de
	seu público-alvo: professores de sistemas públicos, estudantes de licenciaturas e de pós-
	graduação, professores e pesquisadores de várias áreas que atuam no ensino público superior.
</p>
<p class="apresentacao">
	Os eventos realizados possibilitaram a publicação de artigos em periódicos e
	publicação de livros. O livro referente ao primeiro congresso gerou demandas de reimpressão. Do
	evento de 2016 foi organizado o Dossiê <b>“Dermeval Saviani: Cinquenta Anos de Trabalho e
	Educação”</b> publicado em 2017 na Revista Interface.
</p>
<p class="apresentacao">
	Nesse sentido, o Congresso de 2018 objetiva a continuidade dos estudos, reflexões
	e apropriações do coletivo da Pedagogia Histórico-crítica visando o enfrentamento dos processos
	de sucateamento e desvalorização da escola pública e do trabalho docente.
</p>
<p class="apresentacao">
	Tendo por base o cenário político-econômico configurado nos últimos anos no Brasil,
	em especial a partir de 2013 e culminando em 2016 com o impeachment da presidente Dilma
	Rousseff, é importante destacar que a educação escolar pública foi alvejada e um forte processo
	de precarização na formação dos estudantes e no trabalho docente foi instalado, a começar pelos
	cortes de verbas por ações políticas implementadas pelo Ministério da Educação e, também, por
	ações das Secretarias Estaduais e Municipais de Educação. A desvalorização da escola pública e
	da formação de professores foi agravada. Projetos em defesa de corte de investimentos na
	educação pública, em defesa da retirada de direitos conquistados pela categoria docente (por
	exemplo, o não cumprimento do piso nacional alegando a falta de verbas, vide os Estados do Rio
	de Janeiro, Paraná), projetos em defesa do aumento de idade mínima para a aposentadoria dos
	docentes, projetos em defesa do aumento da contribuição previdenciária, projetos em defesa do
	ensino religioso obrigatório na grade curricular, projetos em defesa da privatização das
	universidades públicas, projetos em defesa de ‘escolas sem partidos’, projetos e ações em defesa
	do fechamento de escolas, da superlotação de alunos por sala de aula e demissão de professores
	(vide o Estado de São Paulo), configurou-se em momento histórico desolador e desumanizador. É
	nesse movimento de retrocessos sociais que o Congresso <b>“Pedagogia Histórico-Crítica: em
	defesa da Escola Pública e Democrática em tempos de Projetos de ‘Escolas sem Partidos”</b>
	foi estruturado e materializado coletivamente por membros da Comissão Organizadora (composta por
	professores e estudantes de graduação e pós-graduação da FCT/UNESP e da UNOESTE; professores
	representantes da Secretaria Municipal de Educação de Pres. Prudente) e Coordenadoras Gerais do
	Congresso.
</p>

